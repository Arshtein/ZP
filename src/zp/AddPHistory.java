    public float getRazmer()
    {
        float res = (Float.parseFloat(TimeField.getText()) * Float.parseFloat(StField.getText())*(1+(Float.parseFloat(PremField.getText())/100)) * (1-(Float.parseFloat(NalogField.getText())/100)));
        return res;
    }
